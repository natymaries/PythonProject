class Dreptunghi():
    lungime = 10
    latime = 5
    aria = lungime * latime
    perimetru = lungime * 2 + latime * 2

    def __init__(self, lungime, latime):
        self.lungime = lungime
        self.latime = latime

    def aria(self):
        self.aria = aria
        return aria

    def perimetru(self):
        self.perimetru = perimetru
        return perimetru
